#!/usr/bin/env node
/**
 * Generates a sample evidence packet PDF for the marketing site.
 * Uses PDFKit (pure Node.js, no browser dependency).
 * Output: apps/web/public/sample-evidence-packet.pdf
 */
const PDFDocument = require("pdfkit");
const fs = require("fs");
const path = require("path");

const OUTPUT = path.join(__dirname, "..", "public", "sample-evidence-packet.pdf");
const PACKET_ID = "PKT-2026-03-19-00847";
const TOTAL_PAGES = 5;

// Colors
const C = {
  text: "#1a1a1a",
  muted: "#6b7280",
  dim: "#9ca3af",
  border: "#e5e7eb",
  green: "#16a34a",
  amber: "#d97706",
  accent: "#3b6ef8",
  bg: "#f9fafb",
};

// Layout
const L = {
  left: 60,
  right: 552,
  top: 72,
  bottom: 720,
  width: 492,
};

let currentPage = 0;

// ── Helpers ──────────────────────────────────────────────────────────────

function drawWatermark(doc) {
  doc.save();
  doc.opacity(0.06);
  doc.fontSize(80).font("Helvetica-Bold");
  doc.translate(306, 396);
  doc.rotate(-45, { origin: [0, 0] });
  doc.fillColor("#9ca3af").text("SAMPLE", -180, -30, { width: 360, align: "center" });
  doc.restore();
}

function drawFooter(doc) {
  const y = L.bottom + 20;
  doc.save();
  // Separator
  doc.moveTo(L.left, y).lineTo(L.right, y).lineWidth(0.5).strokeColor(C.border).stroke();
  // Left
  doc.fontSize(7).font("Helvetica").fillColor(C.dim);
  doc.text("Generated by Kontext  |  getkontext.com", L.left, y + 8);
  // Center
  doc.text(`Page ${currentPage} of ${TOTAL_PAGES}`, L.left, y + 8, { width: L.width, align: "center" });
  // Right
  doc.text(PACKET_ID, L.left, y + 8, { width: L.width, align: "right" });
  // Disclaimer
  doc.fontSize(6).fillColor(C.dim);
  doc.text(
    "This document does not constitute legal advice or guarantee regulatory compliance. Consult qualified legal counsel for compliance certification.",
    L.left, y + 22, { width: L.width, align: "center" }
  );
  doc.restore();
}

function newPage(doc) {
  if (currentPage > 0) doc.addPage();
  currentPage++;
  drawWatermark(doc);
  drawFooter(doc);
}

function sectionLabel(doc, y, text) {
  doc.fontSize(8).font("Helvetica-Bold").fillColor(C.muted);
  doc.text(text.toUpperCase(), L.left, y);
  return y + 18;
}

function keyValue(doc, x, y, key, value, opts = {}) {
  doc.fontSize(8).font("Helvetica").fillColor(C.dim).text(key, x, y);
  const font = opts.mono ? "Courier" : "Helvetica-Bold";
  const size = opts.mono ? 9 : 10;
  const color = opts.color || C.text;
  doc.fontSize(size).font(font).fillColor(color).text(value, x, y + 12);
  return y + 30;
}

function badge(doc, x, y, text, color) {
  const width = doc.fontSize(7).font("Helvetica-Bold").widthOfString(text.toUpperCase()) + 12;
  doc.save();
  doc.roundedRect(x, y, width, 16, 3).fillColor(color).opacity(0.12).fill();
  doc.opacity(1).fontSize(7).font("Helvetica-Bold").fillColor(color);
  doc.text(text.toUpperCase(), x + 6, y + 4);
  doc.restore();
  return width;
}

function hrule(doc, y) {
  doc.moveTo(L.left, y).lineTo(L.right, y).lineWidth(0.5).strokeColor(C.border).stroke();
  return y + 12;
}

function tableRow(doc, y, cols, widths, opts = {}) {
  const isHeader = opts.header;
  const font = isHeader ? "Helvetica-Bold" : "Helvetica";
  const size = isHeader ? 7.5 : 9;
  const color = isHeader ? C.muted : C.text;
  let x = L.left;
  doc.fontSize(size).font(font).fillColor(color);
  for (let i = 0; i < cols.length; i++) {
    const cellOpts = { width: widths[i], lineBreak: true };
    if (opts.colors && opts.colors[i]) doc.fillColor(opts.colors[i]);
    else doc.fillColor(color);
    if (opts.fonts && opts.fonts[i]) doc.font(opts.fonts[i]).fontSize(opts.fontsizes ? opts.fontsizes[i] : size);
    else doc.font(font).fontSize(size);
    doc.text(cols[i], x, y, cellOpts);
    x += widths[i];
  }
  return y + (opts.rowHeight || 18);
}

