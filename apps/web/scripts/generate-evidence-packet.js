// ── Main ─────────────────────────────────────────────────────────────────

function main() {
  console.log("[generate-evidence-packet] Generating PDF...");

  const doc = new PDFDocument({
    size: "letter",
    margins: { top: L.top, bottom: 72, left: L.left, right: 60 },
    info: {
      Title: "Payment Decision Evidence Packet - Sample",
      Author: "Kontext (getkontext.com)",
      Subject: "Sample compliance evidence packet for a $48,200 USDC vendor payout",
      Keywords: "compliance, evidence, payment, BSA, OFAC, audit, Kontext",
      Creator: "Kontext Evidence Export",
    },
  });

  const stream = fs.createWriteStream(OUTPUT);
  doc.pipe(stream);

  page1(doc);
  page2(doc);
  page3(doc);
  page4(doc);
  page5(doc);

  doc.end();

  stream.on("finish", () => {
    const size = fs.statSync(OUTPUT).size;
    console.log(`[generate-evidence-packet] Written to ${OUTPUT} (${(size / 1024).toFixed(1)} KB)`);
  });
}

main();
