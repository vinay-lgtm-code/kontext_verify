// Layout constants — US Letter
const PAGE_W = 612;
const PAGE_H = 792;
const M = { top: 56, bottom: 56, left: 52, right: 52 };
const CONTENT_W = PAGE_W - M.left - M.right;
const FOOTER_Y = PAGE_H - M.bottom + 10;

let pageNum = 0;

// ─── Low-level helpers ───────────────────────────────────────────────────

/** Draw text at exact position. lineBreak defaults to FALSE to prevent auto-pagination. */
function t(doc, str, x, y, opts = {}) {
  doc.save();
  doc.font(opts.font || "Helvetica")
    .fontSize(opts.size || 9)
    .fillColor(opts.color || C.text);
  doc.text(str, x, y, {
    width: opts.width || CONTENT_W - (x - M.left),
    lineBreak: !!opts.wrap,
    lineGap: opts.lineGap || 0,
    align: opts.align || "left",
  });
  doc.restore();
}

/** Draw a horizontal rule */
function hr(doc, y, opts = {}) {
  doc.save();
  doc.moveTo(opts.x || M.left, y)
    .lineTo(opts.x2 || (PAGE_W - M.right), y)
    .lineWidth(opts.w || 0.5)
    .strokeColor(opts.color || C.borderLight)
    .stroke();
  doc.restore();
}

/** Section label: uppercase, small, bold, muted */
function label(doc, y, str) {
  t(doc, str.toUpperCase(), M.left, y, {
    font: "Helvetica-Bold", size: 7.5, color: C.muted,
  });
  return y + 16;
}

/** Key-value pair, returns y after */
function kv(doc, x, y, key, val, opts = {}) {
  t(doc, key, x, y, { size: 7.5, color: C.dim });
  t(doc, val, x, y + 11, {
    font: opts.mono ? "Courier" : "Helvetica-Bold",
    size: opts.mono ? 8.5 : 9.5,
    color: opts.color || C.text,
  });
  return y + 28;
}

/** Simple 2-col table row. Values wrap if they exceed column width. */
function row2(doc, y, k, v, opts = {}) {
  const kw = opts.kw || 140;
  t(doc, k, M.left, y, { size: 8.5, color: C.text });
  t(doc, v, M.left + kw, y, {
    font: opts.vfont || "Helvetica",
    size: opts.vsize || 8.5,
    color: opts.vcolor || C.muted,
    width: CONTENT_W - kw,
    wrap: true,
  });
  return y + (opts.h || 18);
}

/** Draw SAMPLE watermark */
function watermark(doc) {
  doc.save();
  doc.opacity(0.04);
  doc.font("Helvetica-Bold").fontSize(90).fillColor(C.dim);
  doc.translate(PAGE_W / 2, PAGE_H / 2);
  doc.rotate(-40, { origin: [0, 0] });
  doc.text("SAMPLE", -200, -40, { width: 400, align: "center" });
  doc.restore();
}

/** Draw page footer */
function footer(doc) {
  hr(doc, FOOTER_Y - 6);
  t(doc, "Generated by Kontext  |  getkontext.com", M.left, FOOTER_Y, {
    size: 6.5, color: C.dim,
  });
  t(doc, `Page ${pageNum} of ${TOTAL_PAGES}`, M.left, FOOTER_Y, {
    size: 6.5, color: C.dim, width: CONTENT_W, align: "center",
  });
  t(doc, PACKET_ID, M.left, FOOTER_Y, {
    size: 6.5, color: C.dim, font: "Courier", width: CONTENT_W, align: "right",
  });
  t(doc, "This document does not constitute legal advice or guarantee regulatory compliance. Consult qualified legal counsel.", M.left, FOOTER_Y + 12, {
    size: 5.5, color: C.dim, width: CONTENT_W, align: "center", wrap: true,
  });
}

/** Start a new page */
function page(doc) {
  doc.addPage();
  pageNum++;
  watermark(doc);
  footer(doc);
}

/** Draw a filled rounded rect behind content */
function bgBox(doc, x, y, w, h, color) {
  doc.save();
  doc.roundedRect(x, y, w, h, 3).fillColor(color).fill();
  doc.restore();
}

/** Draw a status pill */
function pill(doc, x, y, text, color, bgColor) {
  const pw = doc.font("Helvetica-Bold").fontSize(7).widthOfString(text) + 10;
  doc.save();
  doc.roundedRect(x, y, pw, 14, 7).fillColor(bgColor).fill();
  doc.font("Helvetica-Bold").fontSize(7).fillColor(color);
  doc.text(text, x + 5, y + 3, { lineBreak: false });
  doc.restore();
  return pw;
}

